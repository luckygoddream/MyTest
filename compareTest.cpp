//比较运算符
#include "iostream"
using namespace std;
int main1(){
    // == != > <  >= <=
    int a = 10;
    int b = 20;
    cout << (b == a) << endl;
    cout << (b != a) << endl;
    cout << (b > a) << endl;
    cout << (b < a) << endl;
    cout << (b >= a) << endl;
    cout << (b <= a) << endl;



    return 0;
}

